{
    char tx_buffer[BUFFER_LEN];
    va_list args;
    va_start(args, format);
    int usbTxLength = vsnprintf(tx_buffer, BUFFER_LEN, format, args);
    va_end(args);

    if (usbTxLength > 0 && usbTxLength < BUFFER_LEN)
    {
        CDC_Transmit_FS((uint8_t *)tx_buffer, usbTxLength);
    }
}
